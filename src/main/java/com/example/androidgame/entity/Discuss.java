package com.example.androidgame.entity;

public class Discuss {
    int discussId;
    String discussTitle;
    String discussDetail;
    String discussImage;
    String discussType;

    public int getDiscussId() {
        return discussId;
    }

    public void setDiscussId(int discussId) {
        this.discussId = discussId;
    }

    public String getDiscussTitle() {
        return discussTitle;
    }

    public void setDiscussTitle(String discussTitle) {
        this.discussTitle = discussTitle;
    }

    public String getDiscussDetail() {
        return discussDetail;
    }

    public void setDiscussDetail(String discussDetail) {
        this.discussDetail = discussDetail;
    }

    public String getDiscussImage() {
        return discussImage;
    }

    public void setDiscussImage(String discussImage) {
        this.discussImage = discussImage;
    }

    public String getDiscussType() {
        return discussType;
    }

    public void setDiscussType(String discussType) {
        this.discussType = discussType;
    }
}
