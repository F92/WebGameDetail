package com.example.androidgame.entity;

public class Discuss {
    int discuss_id;
    String discuss_title;
    String discuss_detail;
    String discuss_image;
    String discuss_type;

    public int getDiscuss_id() {
        return discuss_id;
    }

    public void setDiscuss_id(int discuss_id) {
        this.discuss_id = discuss_id;
    }

    public String getDiscuss_title() {
        return discuss_title;
    }

    public void setDiscuss_title(String discuss_title) {
        this.discuss_title = discuss_title;
    }

    public String getDiscuss_detail() {
        return discuss_detail;
    }

    public void setDiscuss_detail(String discuss_detail) {
        this.discuss_detail = discuss_detail;
    }

    public String getDiscuss_image() {
        return discuss_image;
    }

    public void setDiscuss_image(String discuss_image) {
        this.discuss_image = discuss_image;
    }

    public String getDiscuss_type() {
        return discuss_type;
    }

    public void setDiscuss_type(String discuss_type) {
        this.discuss_type = discuss_type;
    }
}
